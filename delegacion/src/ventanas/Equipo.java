    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Equipos e = new Equipos(ej.getText().toUpperCase(), marca.getText().toUpperCase(), modelo.getText().toUpperCase(), descripcion.getText());
        boolean alta = e.altaEquipo();

        if (alta == true) {
            JOptionPane.showMessageDialog(null, "Equipo dado de alta correctamente", "Alta Equipo", JOptionPane.INFORMATION_MESSAGE);
            pcs.setRowCount(0);
            limpiar();

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tPcsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tPcsMouseClicked
        pcs = (DefaultTableModel) tPcs.getModel();
        int index = tPcs.getSelectedRow();
        ej.setText(tPcs.getValueAt(index, 0).toString());
        marca.setText(tPcs.getValueAt(index, 1).toString());
        modelo.setText(tPcs.getValueAt(index, 2).toString());
        descripcion.setText(equipos.get(index).getDescripcion());
    }//GEN-LAST:event_tPcsMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        pcs = (DefaultTableModel) tPcs.getModel();
        int index = tPcs.getSelectedRow();

        Equipos equipoM = new Equipos(ej.getText().toUpperCase(), marca.getText().toUpperCase(), modelo.getText().toUpperCase(), descripcion.getText().toUpperCase());

        if (equipoM.modicarEquipo(tPcs.getValueAt(index, 0).toString()) == true) {
            JOptionPane.showMessageDialog(null, "Equipo Modificado");
            pcs.setRowCount(0);
            limpiar();

        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        pcs = (DefaultTableModel) tPcs.getModel();
        int index = tPcs.getSelectedRow();

        if (Equipos.borrarEquipo(tPcs.getValueAt(index, 0).toString()) == true) {
            JOptionPane.showMessageDialog(null, "Equipo Borrado");
            pcs.setRowCount(0);
            limpiar();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

