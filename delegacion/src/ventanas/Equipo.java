    public void limpiar() {

        ej.setText("");
        marca.setText("");
        modelo.setText("");
        descripcion.setText("");
        filtro.setText("");
        pcs.setRowCount(0);
        listarEquipos();
    }

    //listar equipos en table
    private void listarEquipos() {
        pcs = (DefaultTableModel) tPcs.getModel();
        equipos = Equipos.listarPcs();

        equipos.forEach((e) -> {
            pcs.insertRow(pcs.getRowCount(), new Object[]{e.getEj(), e.getMarca(), e.getModelo()
            });
        });
    }

    private void listarEquipo(String ej) {
        pcs.setRowCount(0);
        pcs = (DefaultTableModel) tPcs.getModel();
        equipo = Equipos.buscarPC(ej);

        if (equipo == null) {
            listarEquipos();
        } else {
            pcs.insertRow(pcs.getRowCount(), new Object[]{
                equipo.getEj(), equipo.getMarca(), equipo.getModelo()
            });
            equipos.clear();
            equipos.add(equipo);
        }
    }

    private void autoCompletado() {

        Equipos.autoCompletar(filtro);

    }

